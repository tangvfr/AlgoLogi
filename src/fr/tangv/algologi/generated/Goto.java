package fr.tangv.algologi.generated;

public class Goto {

	private int x,y;
	private String direction;
	
	public Goto(int x, int y, String direction) {
		this.direction = direction;
		this.x = x;
		this.y = y;
	}
	
	public String getDirection() {
		return direction;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
}
